@app.route('/add', methods=['POST', 'GET'])
def add_cafe():
    form = CafeForm()
    if form.validate_on_submit():
        print("True")
        with open('cafe-data.csv', 'a') as csv_file:
            csv_cursor = csv.writer(csv_file, delimiter=',')
            list_of_rows = []
            for (row, col) in form.data.items():
                list_of_rows.append(col)
            # print(list_of_rows[0:-2])
            csv_cursor.writerow(list_of_rows[0:-2])
    return render_template('add.html', form=form)


